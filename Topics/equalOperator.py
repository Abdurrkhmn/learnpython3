# Equal operator
"""Operator yang sama, ==, 
digunakan untuk membandingkan dua nilai, variabel, atau ekspresi untuk menentukan apakah keduanya sama."""

if 'Yes' == 'Yes':
  # evaluates to True
  print('They are equal')

if (2 > 1) == (5 < 10):
  # evaluates to True
  print('Both expressions give the same result')

c = '2'
d = 2

if c == d:
  print('They are equal')
else:
  print('They are not equal')